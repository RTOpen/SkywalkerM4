/**************************************************************************************************
 * @fn      key_hw_init
 *
 * @brief   Initilize Key Service
 *
 * @param   none
 *
 * @return  None
 **************************************************************************************************/
void key_hw_init(void)
{
    KEY_BIND_DIR;
    KEY_BIND_PU;
}

/**************************************************************************************************
 * @fn      key_scan
 *
 * @brief   Read the current value of a key
 *
 * @param   None
 *
 * @return  keys - current keys status
 **************************************************************************************************/
uint8_t key_scan(void)
{
    uint8_t keys = 0;

    if(KEY_BIND_IN)
    {
        keys |= KEY_SW_BIND;
    }

    return keys;
}
